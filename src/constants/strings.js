/* ────────────────────────────────────────────────────────────
 * Helper `pick(lang, 'fiche.tabAnalysis')` — accès pointé pratique
 * avec fallback FR si clé manquante en EN (et vice-versa).
 * ──────────────────────────────────────────────────────────── */
export function pick(lang, path, vars) {
  const walk = (obj) => {
    try {
      return path.split(".").reduce((acc, k) => (acc == null ? undefined : acc[k]), obj);
    } catch { return undefined; }
  };
  let v = walk(STRINGS[lang]);
  if (v == null) v = walk(STRINGS.fr); // fallback FR
  if (typeof v === "string" && vars) {
    Object.keys(vars).forEach((k) => {
      v = v.replace(new RegExp(`\\{${k}\\}`, "g"), vars[k]);
    });
  }
  return v == null ? path : v;
}

export default STRINGS;
